namespace Telepay\FinancialApiBundle\Entity;

class Service {

    private $id;
    private $name;
    private $role;

    public function __construct($id, $name, $role){
        $this->id=$id;
        $this->name=$name;
        $this->role=$role;
    }

    /**
     * @return mixed
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @return mixed
     */
    public function getRole()
    {
        return $this->role;
    }

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }


}